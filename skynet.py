def load_proxies(filename="proxies.txt"):
    proxies = []
    try:
        with open(filename, "r") as f:
            for line in f:
                proxy = line.strip()
                if proxy:
                    proxies.append(proxy)
    except FileNotFoundError:
        print(f"Proxy file '{filename}' not found. Running without proxies.")
    return proxies

